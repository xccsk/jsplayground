// While loop

/* 
In JavaScript, a "while" loop is used to repeatedly execute a block of code while a certain condition is true. Here's the basic syntax for a "while" loop:
*/

while (condition) {
  // code to execute while condition is true
}

//do while

/*
a "do...while" loop will always execute the code inside the curly braces at least once, regardless of whether the condition is true or false.
 */

do {
  // code to execute at least once
} while (condition);
